package com.jhta.bonfire.vo;

public class FbhitsVo {

	private int num;
	private String id;
	public FbhitsVo() {}
	public FbhitsVo(int num, String id) {
		super();
		this.num = num;
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
